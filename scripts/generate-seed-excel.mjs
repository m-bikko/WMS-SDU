function addSheet(name, data, colWidths) {
    const ws = XLSX.utils.json_to_sheet(data);
    if (colWidths) ws["!cols"] = colWidths.map(w => ({ wch: w }));
    XLSX.utils.book_append_sheet(wb, ws, name);
}

